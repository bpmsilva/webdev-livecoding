// Strings e manipulação de strings
// Neste exemplo, exploramos como manipular strings em JavaScript.

// Criando uma string
let minhaString = "Olá, Mundo!";

// Exibindo a string no console'
console.log(minhaString);

// Obtendo o comprimento da string
console.log("Comprimento da string:", minhaString.length);

// Acessando caracteres individuais
console.log("Primeiro caractere:", minhaString[0]);

// Convertendo para maiúsculas e minúsculas
console.log("Maiúsculas:", minhaString.toUpperCase());
console.log("Minúsculas:", minhaString.toLowerCase());
